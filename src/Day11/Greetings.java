package Day11;

public class Greetings 
{
	
	//1.Method No Params and No return value
	
	
	void m1()
	{
		System.out.println("Hi there");
		
	}
	
	
	//2.Method No Params Return value
	
	String m2()
	{
		return "hello";
		
	}
	
	
	//3.Method takes params ad no returns
	
	void m3(String name)
	{
		System.out.println(name);
	}

	
	//4.Method takes params and returns the value
	
	String m4(String name) 
	
	{
		return name;
		
		
	}
}
