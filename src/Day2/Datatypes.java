package Day2;

public class Datatypes {

	public static void main(String[] args) {
		
		//primitive data types the keyword starts from the lower case
		final int a=100;//if we use the final command it is a constant value cannot be changed,
		System.out.println(a);
		
		float b=10.7F;    //needs to use float at the last
		System.out.println(b);
		
		
		double c=10.88888;
		System.out.println(c);
		
		char g= 'A';
		System.out.println(g);
		
		
		
		long l = 45677866555l; // need to use literal at the last
		System.out.println(l);
		
        //Non primitive data type starts from the upper case
		String name="Testing";
		System.out.println(name);
		
	}
}
