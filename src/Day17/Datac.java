package Day17;

public class Datac 
{

	public static void main(String[] args)
	{
		
		int a=10;
		double d=10.4;
		boolean b=true;
		char c='A';
		//To Convert any datatype to the string datatype
		String s;
		
		s=String.valueOf(a);
		System.out.println(s);
		
		s=String.valueOf(d);
		System.out.println(s);
		
		s=String.valueOf(b);
		System.out.println(s);
		
		s=String.valueOf(c);
		System.out.println(s);
		
	}
}
