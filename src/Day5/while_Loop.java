package Day5;

public class while_Loop {

	public static void main(String[] args) {
		
		int i=0;        //intialization
		
		while (i<=5)    //condition (How many times needs to print)
		{     
			System.out.println(i);
			i++;               //increment
			
		}
		
		
		
		
		
		while (i<=10) {
			
			System.out.println("Say Hello");
			i++;
			
		}
		
	}
	
	
}

